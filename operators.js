let a = 5
let b = 6

//Arithmetic
console.log("Sum =", a+b)
console.log("Product =", a*b)
console.log("Difference =", a-b)
console.log("Division =", a/b)
console.log("Modulus =", a%b)

//Comparison 
console.log("Is equal=", 10 == "10")   //true
console.log("Is equal=", 10 === "10")  //false
console.log("Is not equal=", 10 != "10")  //false
console.log("Is not equal=", 10 !== "10")  //true

//Logical
console.log(a && b)
console.log(a || b)
console.log(b && a)
console.log('Gunnu' && 'Anku')
console.log(!('Gunnu' && 'Anku'))
console.log('Gunnu' || 'Anku')
console.log(!('Gunnu' || 'Anku'))   //false
 console.log(0.1 + 0.2)
 console.log(0.1 + 0.1)
 console.log((0.1 + 0.2).toFixed(3))